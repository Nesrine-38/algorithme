# création d'une pile
stack = []

# Ajouter des valeurs dans une pile
stack.append(1)
stack.append(2)
stack.append(3)
stack.append(4)
stack.append(5)

# Affichage des éléments de la pile 
print(stack)

# suppression d'un élément de la pile
item = stack.pop()
